# ---------------------------------------------------------------------------
# Hash pattern heuristics for auto-identification (server-side fallback)
# Used when hashcat --identify is not available
# ---------------------------------------------------------------------------

_PATTERNS: List[Tuple[re.Pattern, int, str]] = [
    (re.compile(r"^\$2[ayb]\$\d{2}\$.{53}$"),                       3200, "bcrypt"),
    (re.compile(r"^\$6\$.{8,16}\$.{86}$"),                          1800, "sha512crypt"),
    (re.compile(r"^\$5\$.{8,16}\$.{43}$"),                          7400, "sha256crypt"),
    (re.compile(r"^\$1\$.{8,16}\$.{22}$"),                           500, "md5crypt"),
    (re.compile(r"^[a-fA-F0-9]{32}$"),                                 0, "MD5"),
    (re.compile(r"^[a-fA-F0-9]{40}$"),                               100, "SHA1"),
    (re.compile(r"^[a-fA-F0-9]{64}$"),                              1400, "SHA-256"),
    (re.compile(r"^[a-fA-F0-9]{128}$"),                             1700, "SHA-512"),
    (re.compile(r"^[a-fA-F0-9]{96}$"),                             10800, "SHA-384"),
    (re.compile(r"^[a-fA-F0-9]{56}$"),                              1300, "SHA-224"),
    (re.compile(r"^[a-fA-F0-9]{8}$"),                              11500, "CRC32"),
    (re.compile(r"^\$P\$[./0-9A-Za-z]{31}$"),                        400, "phpass"),
    (re.compile(r"^\$apr1\$"),                                       1600, "Apache APR1"),
    (re.compile(r"^[A-Z0-9]{32}:[a-zA-Z0-9+/=]{24}$"),             1000, "NTLM (with username)"),
    (re.compile(r"^[a-fA-F0-9]{32}:[a-fA-F0-9]{32}$"),             1000, "NTLM"),
    (re.compile(r"^\$krb5tgs\$23\$"),                              13100, "Kerberos TGS-REP"),
    (re.compile(r"^\$krb5asrep\$23\$"),                             18200, "Kerberos AS-REP"),
    (re.compile(r"^v=1,id="),                                       22000, "WPA PMKID+EAPOL"),
]


def identify_hash_modes(sample: str) -> List[dict]:
    """
    Return list of likely {mode_id, name} dicts for a given hash sample.
    Tries regex heuristics; returns empty list if nothing matches.
    """
    sample = sample.strip()
    results = []
    for pattern, mode_id, name in _PATTERNS:
        if pattern.match(sample):
            results.append({"mode_id": mode_id, "name": name})
    return results
